export function getRegionNames(): string[] {
  return RUSSIAN_REGIONS.map((r) => r.name).sort((a, b) => a.localeCompare(b, 'ru'));
}

export function getCitiesByRegion(regionName: string): string[] {
  const region = RUSSIAN_REGIONS.find((r) => r.name === regionName);
  return region ? [...region.cities].sort((a, b) => a.localeCompare(b, 'ru')) : [];
}

export function findRegionByCity(cityName: string): string | null {
  for (const region of RUSSIAN_REGIONS) {
    if (region.cities.includes(cityName)) {
      return region.name;
    }
  }
  return null;
}
